def shiftrows(message: list):
    message_final = []
    for matrice in message:
        mat_temp = np.zeros((4, 4), dtype=int)
        for i in range(4):
            mat_temp[i] = np.roll(matrice[i], -i)
        message_final.append(mat_temp % 256)
    return message_final


def inv_shiftrows(message: list):
    message_final = []
    for matrice in message:
        mat_temp = np.zeros((4, 4), dtype=int)
        for i in range(4):
            mat_temp[i] = np.roll(matrice[i], i)
        message_final.append(mat_temp % 256)
    return message_final


