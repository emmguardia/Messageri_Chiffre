def chiffrement(message: str, shared_secret: int):
    k1 = list(hashlib.sha256(str(shared_secret + 18).encode()).digest())
    k2 = list(hashlib.sha256(str(shared_secret + 61).encode()).digest())
    k3 = list(hashlib.sha256(str(shared_secret + 74).encode()).digest())
    k4 = list(hashlib.sha256(str(shared_secret + 3).encode()).digest())

    liste_carres, longueur = transformer_en_carres(message)

    cle1 = np.array(k1[:16]).reshape(4, 4)
    cle2 = np.array(k2[:16]).reshape(4, 4)
    cle3 = np.array(k3[:16]).reshape(4, 4)
    cle4 = np.array(k4[:16]).reshape(4, 4)

