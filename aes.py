        etape = shiftrows([nouvelle])[0]
        etape = mix_column(etape)

        etape = etape ^ cle4

        nouvelle = etape.copy()
        for l in range(4):
            for c in range(4):
                nouvelle[l, c] = sbox4[etape[l, c]]
