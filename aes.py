def transformer_en_carres(message):
    liste_chiffres = list(message.encode('utf-8')) 
    longueur_originale = len(liste_chiffres) 
    while len(liste_chiffres) % 16 != 0:
        liste_chiffres.append(32) 
    liste_carres = []
    for i in range(0, len(liste_chiffres), 16):
        groupe = liste_chiffres[i:i+16]
        carre = np.array(groupe).reshape(4, 4)
        liste_carres.append(carre)

    return liste_carres, longueur_originale


